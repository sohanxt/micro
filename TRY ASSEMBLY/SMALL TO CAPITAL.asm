INCLUDE 'EMU8086.INC'
.MODEL SMALL
.STACK 100H
.DATA
STR DB 'bangladesh$'

.CODE
MAIN PROC
    
MOV AX,@DATA
MOV DS,AX

MOV SI,OFFSET STR
MOV CX,10

L1: 
CMP [SI] ,'a'
JB SKIP
CMP [SI] ,'z'
JA SKIP
SUB [SI],32  

SKIP:
INC SI
LOOP L1

MOV AH,9
LEA DX,STR
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN


