.MODEL SMALL
.STACK 100H
.DATA

A DB 'NON NEGATIVE $'
B DB 'NEGATIVE $'

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX   

MOV BX,-10

CMP BX,0
JGE NONNEGATIVE 
JMP NEGATIVE


NONNEGATIVE:
MOV AH,9
LEA DX,A
INT 21H
JMP EXIT


NEGATIVE:
MOV AH,9
LEA DX,B
INT 21H


 
 EXIT:   
 MOV AH,4CH
 INT 21H
 MAIN ENDP
END MAIN


