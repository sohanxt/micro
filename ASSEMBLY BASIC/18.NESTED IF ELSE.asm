.MODEL SMALL
.STACK 100H
.DATA

A DB 10,13, 'GREATER THEN 5 $'
B DB 10,13, 'LESS THEN 5 $'
C DB 10,13, 'EQUAL TO 5 $'

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX   

MOV AH,1
INT 21H
MOV BL,AL

CMP BL,53
JE EQUAL 
JG GREATER
JMP LESS


EQUAL:
MOV AH,9
LEA DX,C
INT 21H 
JMP EXIT


GREATER:
MOV AH,9
LEA DX,A
INT 21H
JMP EXIT

LESS:

MOV AH,9
LEA DX,B
INT 21H


 
 EXIT:   
 MOV AH,4CH
 INT 21H
 MAIN ENDP
END MAIN



